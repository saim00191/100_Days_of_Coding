let Percentage: number = 66

if (Percentage >= 90) {
    console.log("Grade: A+")
} else if (Percentage >= 80) {
    console.log("Grade: A")
} else if (Percentage >= 70) {
    console.log("Grade: B")
}else if (Percentage >= 60) {
    console.log("Grade: C")
}else if (Percentage >= 50) {
    console.log("Grade: D")
} else {
    console.log("Grade: Fail")
}
